# Sorting in python .shot() or sorted()
# Lists[], Tuples(), Dictionaries{"":""}, Objects

# ----------------List--------------------
fruits = ["banana", "orange", "apple", "coconut"]
fruits.sort() # Order alphabetic
print(fruits)

numbers = [4,3,2,5,1]
numbers.sort(reverse=True) # 5,4,3,2,1
print(numbers)

